function soma(item1, item2) {
    return item1 + item2;
}

function diminuir(item1, item2) {
    return item1 - item2;
}

function multiplicar(item1, item2) {
    return item1 * item2;
}

function dividir(item1, item2) {
    return item1 / item2;
}

console.log('SOMA: ', soma(5, 6));
console.log('SOMA: ', soma(20, 20));
console.log('SUBTRAÇÃO: ', diminuir(400, 200));
console.log('MULTIPLICAÇÃO: ', multiplicar(5, 10));
console.log('DIVISÃO: ', dividir(10, 10));