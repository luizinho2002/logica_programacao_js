function teste(key) {
    switch (key) {
        case 'Luiz':
            console.log('LUIZ AQUI');
            break;
        case 'Pedro':
            console.log('PEDRO AQUI');
            break;
        case 'José':
            console.log('JOSÉ AQUI');
            break;
        default:
            console.log('QUALQUER OUTRO AQUI');
            break;
    }
}

teste('Luiz');